-- ============================================================
-- One-off fix: отметить ADM-DQDAF7 как использованный
-- Date: 2026-04-25
-- ============================================================
-- Почему нужна правка вручную:
-- Код ADM-DQDAF7 был использован для регистрации админа, но
-- клиент не дождался подтверждения UPDATE is_used=true.
-- PostgREST `.update()` без `.select()` не бросает ошибку, если
-- ни одна строка не затронута — поэтому баг оставался тихим
-- до тех пор, пока в репозитории не появилась проверка.
--
-- Фикс кода: invite_code_repository.dart::markUsed теперь
-- использует .select() и бросает исключение, если обновлено 0
-- строк. В auth_gate.dart вокруг markUsed поставлен локальный
-- try/catch, чтобы админ всё равно попал в приложение, а
-- ошибка ушла в лог, а не заблокировала вход.
-- ============================================================

UPDATE ibadat_invite_codes
   SET is_used = TRUE
 WHERE code IN ('ADM-DQDAF7', 'ADM-4X3WDA')
   AND is_used = FALSE;
